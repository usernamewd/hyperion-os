//! Built-in 8x8 bitmap font.
//!
//! Covers ASCII printable characters (0x20..=0x7E). Each glyph is 8 rows
//! of 8 bits; bit 7 is the leftmost pixel. Anything outside the supported
//! range falls back to a filled square so callers can spot it visually.
//!
//! The glyph data here is a clean-room subset based on the public-domain
//! "8x8 font" common to many hobby kernels (see e.g. dhepper/font8x8).

use crate::display::framebuffer::Framebuffer;

pub struct Font8x8;

impl Font8x8 {
    /// Render a single character at `(x, y)`. Pixels outside the
    /// framebuffer are clipped.
    pub fn draw_char(&self, fb: &mut Framebuffer, x: i32, y: i32, ch: char, rgba: [u8; 4]) {
        let glyph = glyph_for(ch);
        for (row, bits) in glyph.iter().enumerate() {
            for col in 0..8 {
                if (bits >> (7 - col)) & 1 == 1 {
                    let px = x + col;
                    let py = y + row as i32;
                    if px >= 0 && py >= 0 {
                        fb.put_pixel(px as u32, py as u32, rgba);
                    }
                }
            }
        }
    }
}

fn glyph_for(ch: char) -> &'static [u8; 8] {
    let c = ch as u32;
    if !(0x20..=0x7e).contains(&c) {
        return &MISSING;
    }
    &GLYPHS[(c - 0x20) as usize]
}

